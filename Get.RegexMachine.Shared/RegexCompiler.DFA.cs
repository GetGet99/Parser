    // Helper function to calculate the epsilon closure of a set of NFA states
    static HashSet<NFAState> EpsilonClosure(IEnumerable<NFAState> states)
    {
        var closure = new HashSet<NFAState>(states);
        var worklist = new Queue<NFAState>(states);

        while (worklist.Count > 0)
        {
            var state = worklist.Dequeue();
            foreach (var epsilonState in state.Epsilon)
            {
                if (closure.Add(epsilonState))
                {
                    worklist.Enqueue(epsilonState);
                }
            }
        }

        return closure;
    }
    
    public class DFAState(int id, T? value)
    {
        /// <summary>
        /// Range-based transitions stored as sorted, non-overlapping (CharRange → DFAState) pairs.
        /// </summary>
        public List<(CharRange Range, DFAState State)> Transitions { get; } = [];

        /// <summary>Looks up the next state for a given character via range search.</summary>
        public DFAState? this[char c]
        {
            get
            {
                foreach (var (range, state) in Transitions)
                    if (range.Contains(c))
                        return state;
                return null;
            }
        }

        /// <summary>Convenience: walks a string through the DFA.</summary>
        public DFAState? this[string c]
        {
            get
            {
                if (c.Length is 0)
                    return null;
                var next = this[c[0]];
                if (next is null)
                    return null;
                if (c.Length == 1)
                    return next;
                else
                    return next[c[1..]];
            }
        }

        /// <summary>
        /// Adds a transition for the given character range, merging adjacent/overlapping ranges.
        /// </summary>
        public void AddTransition(char from, char to, DFAState target)
        {
            var newRange = new CharRange(from, to);
            for (int i = 0; i < Transitions.Count; i++)
            {
                var (range, state) = Transitions[i];
                if (state == target && range.CanMerge(newRange))
                {
                    // Merge with existing range for same target
                    Transitions[i] = (CharRange.Merge(range, newRange), state);
                    // Merge subsequent adjacent ranges for same target
                    while (i + 1 < Transitions.Count && Transitions[i + 1].State == target && Transitions[i].Range.CanMerge(Transitions[i + 1].Range))
                    {
                        Transitions[i] = (CharRange.Merge(Transitions[i].Range, Transitions[i + 1].Range), target);
                        Transitions.RemoveAt(i + 1);
                    }
                    return;
                }
                if (newRange.To < range.From)
                {
                    Transitions.Insert(i, (newRange, target));
                    return;
                }
            }
            Transitions.Add((newRange, target));
        }

        public int Id { get; } = id;
        public T? Value { get; internal set; } = value;
        public bool IsAccepting => Value != null;
        public override string ToString()
        {
            return $"{Id} ({Value as object ?? "null"}) => {{{string.Join(", ", Transitions.Select(t => t.Range.ToString()))}}}";
        }
    }
}
