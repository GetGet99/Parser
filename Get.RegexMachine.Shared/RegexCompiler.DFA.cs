partial class RegexCompiler<T>
{
    static DFAState ConvertToDFA(NFAState startState, RegexConflictBehavior conflictBehavior)
    {
        var unprocessedStates = new Queue<HashSet<NFAState>>();
        var processedStates = new Dictionary<HashSet<NFAState>, DFAState>(new HashSetEqualityComparer());
        var nextId = 0;

        // Start with the epsilon closure of the start state
        var initialState = EpsilonClosure([startState]);
        unprocessedStates.Enqueue(initialState);

        while (unprocessedStates.Count > 0)
        {
            var currentState = unprocessedStates.Dequeue();

            // Check if the state has already been processed
            if (!processedStates.TryGetValue(currentState, out var dfaState))
            {
                // Create a new DFA state
                processedStates[currentState] = dfaState = new DFAState(nextId++, GetValue(currentState, conflictBehavior));
            }

            // Collect all (range, target) pairs from all NFA states in the current set
            var rangeTargets = currentState
                .SelectMany(s => s.Transitions.Select(t => (t.Range, t.Targets)))
                .ToList();

            if (rangeTargets.Count == 0)
                continue;

            // Partition the character space at every range boundary
            // Collect all unique boundary points: each range's From and (To + 1)
            var boundaries = new SortedSet<char>();
            foreach (var (range, _) in rangeTargets)
            {
                boundaries.Add(range.From);
                if (range.To < char.MaxValue)
                    boundaries.Add((char)(range.To + 1));
            }

            // Build sub-ranges between consecutive boundaries
            char? prev = null;
            foreach (var b in boundaries)
            {
                if (prev.HasValue)
                {
                    var subRange = new CharRange(prev.Value, (char)(b - 1));
                    ProcessSubRange(subRange, rangeTargets, dfaState,
                        processedStates, unprocessedStates, conflictBehavior, ref nextId);
                }
                prev = b;
            }
            // Last sub-range from last boundary to char.MaxValue
            if (prev.HasValue)
            {
                var lastRange = new CharRange(prev.Value, char.MaxValue);
                ProcessSubRange(lastRange, rangeTargets, dfaState,
                    processedStates, unprocessedStates, conflictBehavior, ref nextId);
            }
        }

        return processedStates[initialState];
    }

    static void ProcessSubRange(
        CharRange subRange,
        List<(CharRange Range, HashSet<NFAState> Targets)> rangeTargets,
        DFAState dfaState,
        Dictionary<HashSet<NFAState>, DFAState> processedStates,
        Queue<HashSet<NFAState>> unprocessedStates,
        RegexConflictBehavior conflictBehavior,
        ref int nextId)
    {
        // Find which NFA states' ranges overlap this sub-range
        var nextNFAStates = new HashSet<NFAState>();
        foreach (var (range, targets) in rangeTargets)
        {
            if (range.Overlaps(subRange))
            {
                nextNFAStates.UnionWith(targets);
            }
        }

        if (nextNFAStates.Count == 0)
            return;

        var nextState = new HashSet<NFAState>(EpsilonClosure(nextNFAStates));
        if (!processedStates.TryGetValue(nextState, out var a))
        {
            unprocessedStates.Enqueue(nextState);
            processedStates[nextState] = a = new DFAState(nextId++, GetValue(nextState, conflictBehavior));
        }
        dfaState.AddTransition(subRange.From, subRange.To, a);
    }
    // Code Generated by Gemini
    static T? GetValue(HashSet<NFAState> states, RegexConflictBehavior conflictBehavior)
    {
        NFAState? maxState = null;
        int maxOrder = int.MinValue;

        int maxOrderCount = 0;
        foreach (var state in states)
        {
            if (state.IsAccepting)
            {
                if (state.Order > maxOrder)
                {
                    maxOrderCount = 1;
                    maxOrder = state.Order;
                    maxState = state;
                } else if (state.Order == maxOrder)
                {
                    maxOrderCount++;
                }
                // else: we are not considering that rule
            }
        }

        if (maxState == null)
        {
            return default;
        }

        if (conflictBehavior == RegexConflictBehavior.Last)
        {
            return maxState.Value;
        }
        if (maxOrderCount > 1)
        {
            var rules = states.Where(s => s.IsAccepting && s.Order == maxOrder).Select(s => s.Rule).Distinct();
            throw new RegexConflictCompilerException(rules.ToArray());
        }
        return maxState.Value;
    }
    // Code Generated by Gemini
    class HashSetEqualityComparer : IEqualityComparer<HashSet<NFAState>>
    {
        public bool Equals(HashSet<NFAState>? x, HashSet<NFAState>? y)
        {
            if (x == null || y == null)
            {
                return x == y;
            }

            return x.SetEquals(y);
        }

        public int GetHashCode(HashSet<NFAState> obj)
        {
            unchecked
            {
                int hash = obj.Count;
                foreach (var state in obj)
                {
                    hash ^= state.GetHashCode();
                }
                return hash;
            }
        }
    }
